#import "UIColor+Theme.h"

@implementation UIColor (Theme)

+(UIColor *)themeBlack
{
    return RGB(0x08, 0x1f, 0x33);
}

+(UIColor *)themeGray
{
    return RGB(0xa1, 0xaa, 0xb3);
}

+(UIColor *)themeGrayPale
{
    return RGB(0xf2, 0xf4, 0xf5);
}

+(UIColor *)themeRed
{
    return RGB(0xff, 0x5b, 0x4c);
}

+(UIColor *)themeBlue
{
    return RGB(0x29, 0x9c, 0xff);
}

@end
